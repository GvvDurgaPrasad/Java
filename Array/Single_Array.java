package Array;

public class Single_Array
{

	public static void main(String[] args) 
	{
		int a[] = new int[5];
		a[0] = 1;
		a[1] = 10;
		a[2] = 100;
		a[3] = 10000;
		a[4] = 100000;
		System.out.println(a[0]);
		System.out.println(a[1]);
		System.out.println(a[2]);
		System.out.println(a[3]);
		System.out.println(a[4]);
	}

}
